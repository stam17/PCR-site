<h1>About Us</h1>
    <h3>What is Purrfect Cat Rescue?</h3>
    <div class="pgsection">
    Purrfect Cat Rescue is an all-volunteer non-profit (EIN #38-3729165). Our main focus is the adoption of home-fostered (we do not have a shelter or facility) cats & kittens and providing low-cost spay & neuter to the public. ​We currently have only 8 foster homes to care for all the cats and kittens in our program. We do not have any association with Tri-City Animal Services, any local shelters, nor do we receive local, state or national funding. We are a small, private organization with limited scope, serving Fremont, Newark & Union City. 
    </div>

    <h3>Our History</h3>
    <div class="pgsection">
        <p>
        In 2006, five locals who had worked for various animal welfare groups, including Furry Friends Rescue and Ohlone Humane Society, came together across a kitchen table to form Purrfect Cat Rescue. They wanted to operate a home-fostered adoption program, as well as providing the public with low-cost spay and neuter resources—both for tame, companion cats, and free-roaming cats in the community.
        </p> 
        <p>
        No other local group—let alone one this small—aspired to do both of these; and that remains a remarkable accomplishment of Purrfect Cat: Quite literally, thousands of cats and kittens have been spayed and neutered as a result of those original five members' vision. 
        </p>
        <p>
        The group has changed in the eleven years that have since passed, but that vision has remained constant and will as long as we are fortunate enough to serve this community and the cats. 
        </p>
        <p>A Memorial to past president Jean Kneisler.</p>
