    <h3 id="p2">Feral Cats & Strays</h3>
    <br>

    <h2 id="sec4">>Does PCR provide medical care for strays/ferals?</h2>
    <p>
    Purrfect Cat Rescue (or any other rescue we are aware of) cannot provide medical care, urgent or otherwise, for strays and ferals. Call local vets to see if they participate in a "concerned citizen" program for found cats and dogs.
    </p>

    <br>

    <h2 id="sec5">>Does PCR work with feral cats?</h2>
    <p>Purrfect Cat Rescue is not a feral cat organization. Our volunteers cannot trap animals or relocate feral cats. For those interested in beginning a trap/neuter project, we can provide access to low-cost spay and neuter, information on trapping, and resources for the services we ourselves cannot offer the public <a href="snippets\services_spayneuter.html">(more here)</a>.</p>
