    <h2 id="sec1">What is Purrfect Cat Rescue?</h2>
    <p>
    Purrfect Cat Rescue is an all-volunteer non-profit (EIN #38-3729165). Our main focus is the adoption of home-fostered (we do not have a shelter or facility) cats & kittens and providing low-cost spay & neuter to the public. â€‹We currently have only 8 foster homes to care for all the cats and kittens in our program. We do not have any association with Tri-City Animal Services, any local shelters, nor do we receive local, state or national funding. We are a small, private organization with limited scope, serving Fremont, Newark & Union City. 
    </p>

