    <h2 id="sec2">Can PCR take my cats/kittens?</h2>
    <p>
      We currently have only 8 foster homes to care for all the cats and kittens in our program. This greatly limits the cats we ourselves can care for. Given the number of requests of assistance we receive (particularly during kitten season), we are only able to meet a small part of that need. 

      If we are able to offer assistance, please respond right away, as there will always be someone else waiting for that offer of help. This rule applies to any of the other organizations we might refer you to; your request of aid is just one of the many they receive so follow up quickly when they offer their support.
    </p>

    <br>

    <h2 id="sec3">What is the Public Assist Program?</h2>
    <p>
      Because of our limited capaity, we make every effort to assist members of the public in adopting out the cats/kittens they have found when they themselves are willing/able to foster the cats in their own homes. Our Public Assist program is outlined <a href="snippets\services_publicassist.html">here</a>
    </p>

