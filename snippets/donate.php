<h3>Grants & Fundraisers</h3>
<p>The core membership of Purrfect Cat are people who excel at the rescue side of rescue work: fostering; running the spay & neuter program; answering e-mails from the public; and hosting our weekly adoption events.  We are sorely lacking in people with the time, skills and inclination required to pursue grant opportunities or fundraisers.  If you have experience in this area, feel free to contact us.</p>

<br>

<h3>Car Donation​</h3>
<p>Purrfect Cat Rescue has joined with The Car Program to begin accepting donations of used vehicles.  The Car Program will pick up your donated vehicle and take care of all the paperwork for you. It couldn't be any easier!  You can even download the donation form  online and fax it in.</p>

<p>Please note that the car donation program does not release the name or contact information of donors to us and therefore we are unable to personally thank them. </p>

<br>

<h3>Material Donations</h3>

<p>For those who cannot open their homes to a cat but want to help, we are always grateful for your donations. Our needs change all the time depending on the number of cats—and ages and requirements of those cats and kittens—that happen to be under our care at a given time.</p>

<p>Because of this, donations of money and gift cards for pet supply stores are the most helpful to us but we know that some people prefer to do their own shopping on our behalf.  Here is a small list of supplies we can use most of the time:</p>

<p>
<li>KMR, Kitten Milk Replacer, powdered</li>
<li>Royal Canin Baby Cat Food, Royal Canin Kitten Food</li>
<li>Canned Kitten/Cat Food</li>
<li>Small Puppy Training Pads</li>
<li>Scratching Posts and Pads</li>
<li>Cat Toys</li>
<li>Hand Sanitizer and Wipes</li>
</p>

<p>All food should be unopened (for safety concerns, anything brought to us open will be discarded) and used scratching posts or kitty condos should only be gently used and fairly clean.</p>

